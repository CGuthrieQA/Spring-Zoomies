package com.qa.springcars.domain;

public class Boat {
	
	// variables
	private boolean floats;

	// constructor
	public Boat(boolean floats) {
		super();
		this.floats = floats;
	}
	
	// getters and setters
	public boolean isFloats() {
		return floats;
	}

	public void setFloats(boolean floats) {
		this.floats = floats;
	}
	
}
